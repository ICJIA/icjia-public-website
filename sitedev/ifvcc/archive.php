<h2 class="h4 rule project first" style="font-weight: 700">Mediation and Family Violence</h2>


<p>In 2006, the Illinois Supreme Court adopted rules governing mediation in family law cases. Illinois Supreme Court Rule 905(b) mandates each judicial circuit to establish a mediation program for all dissolution of marriage and paternity cases involving issues of child custody or visitation. Rule 923(a)(3) mandates that if the parties to a case involving child custody or visitation issues have not reached an agreement regarding those issues at the time of the initial custody case management conference, the parties must attend mediation to resolve those issues prior to litigation. While most judges and attorneys agree that mediation is generally a good thing for most custody and visitation cases, there may be certain cases, such as cases that involve domestic violence, that may require special considerations during mediation or perhaps a waiver of mediation altogether.
