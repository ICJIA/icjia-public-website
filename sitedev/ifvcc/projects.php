In 2014, IFVCC was awarded a renewal of the Arrest Grant, which allows for additional training, implementation and follow-up with local jurisdictions on the protocols. Utilizing the model protocols, the IFVCC Arrest Grant Advisory Statewide Committee are developing toolkits for first responders (911/Dispatchers and Emergency Medical professionals), court personnel (Circuit Clerks office personnel and Court Security/Bailiffs) and Probation. Guidelines will be distributed statewide through the Local FVCCs.  By developing and providing resources and toolkits for other associated disciplines, the coordinated community response effort will be strengthened along with the ongoing implementation of the protocols.</p>




