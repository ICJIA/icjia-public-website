<h2 class="h4 rule project" style="font-weight: 700">Elder Sensitivity Training</h2>
<p>
According to the Illinois Department on Aging, about 4 to 5 percent of senior citizens experience some kind of mistreatment, but only about 1 in 13 cases of elder abuse are ever reported. Of the seniors abused, almost 35 percent were living with their abusers. Of the abusers, 40 percent were children of the victim.</p>
<p>While specialized courts and services are continuing to be established throughout the state of Illinois for victims of domestic violence, the question remains – what is being done for older adults trying to navigate the court, criminal justice and social service systems?</p>
<p>In addition, what resources and training exist for the community (clergy, health care providers, bankers, in-home care workers, nursing home personnel, and social service providers) to better respond to elder abuse?</p>
<p>In order to make those in our communities more aware of the special needs of the elderly, the Illinois Family Violence Coordinating Council’s Responding to Elder Abuse Committee has designed a training format along with a kit to be used by FVCC members to train others to be more sensitive to their elderly clients and associates.</p>
<p>There are few businesses or agencies that do not have any interaction with the older population. And there are employees that do not understand aging issues and, therefore, are not effective in dealing with elderly clients. These employees become frustrated with older adults’ slow movements, misinterpretation of directions and inability to hear everything that is said.</p>
<p>Training with the IFVCC Awareness of the Special Needs of the Elderly Kit for business staff has increased the employees’ sensitivity to the needs of older adults, and changes in the business environment and strategies for communication have occurred, resulting in enhanced customer service. The staff members of these organizations are now equipped with sensitivity, knowledge and skills for dealing with the elderly and have a better understanding of how their elderly clients should be treated. The training also helps participants to identify at-risk older adults and maximize utilization of existing community resources.</p>




<h2 class="h4 rule project" style="font-weight: 700">Schools Respond to Family Violence</h2>

<p>The Illinois Family Violence Coordinating Council created a Schools Respond to Family Violence Project designed to increase school personnel's awareness of family violence issues, to help teachers, staff and school leadership better respond to students affected by family violence, and to prompt implementation of integrated service protocols on family violence. This  Project includes a Guidebook specifically written for school personnel, easy-to-adapt training designs, and ongoing technical assistance and support from the Illinois Family Violence Coordinating Councils staff.</p>
<p>Both the Guidebook and project were developed through an advisory committee and pilot sites throughout Illinois who have implemented Schools Respond to Family Violence programs.</p>
<p>Highlights of the programs included:</p>
<ul>
<li>Schools reported a higher level of confidence and ability among teachers and staff on how to serve students impacted by family violence, including issues of mandated reporting, signs of abuse in teen dating relationships, and talking with students who are witnesses of domestic violence.</li>
<li>High level of cooperation at all sites from local sexual assault agencies, domestic violence agencies, law enforcement, DCFS, and youth-serving agencies resulted in increased trust and communication among these community members.</li>
<li>Significantly increased activities which educate teachers and staff, as well as students and community members on issues relating to family violence and on how to access resources.</li>
<li>A belief that there was an increase in referrals to the local domestic violence and sexual assault centers since the program has been implemented.</li>
</ul>
<p>IFVCC staff and Local Council Coordinators continue to approach new schools each year to engage in the Schools Respond to Family Violence Project.</p>








