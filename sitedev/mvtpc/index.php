    <h3 class="uppercase">
      Welcome to the Illinois Motor Vehicle Theft Prevention Council
    </h3>

    <p>
      The mission of the Illinois MVTPC is to reduce vehicle theft, insurance fraud, and motor vehicle theft-related crimes in Illinois.

  In 1991, the General Assembly established the Illinois Motor Vehicle Theft Prevention Council, an 11-member coalition of representatives from the insurance industry, state's attorneys, and law enforcement officers. It is private and public partnership effectively combating motor vehicle theft and related crimes in Illinois.
    </p>

    <p>
      Grants funds awarded by the Council have improved motor vehicle theft laws enforcement by establishing and supporting multi-jurisdictional task forces, investigative teams, and other anti-theft efforts throughout the state.


    </p>








